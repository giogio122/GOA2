# შექმენი ფუნქცია სახელად filter რომელიც მიიღებს რიცხვს,
# შემდეგ დაადგენს შემოსული ეს რიცხვი ლუწია თუ კენტი
# და დააბრუნებს შესაბამის შედეგს even ან odd

def filter(number):
    if number % 2 == 0:
        return "even"
    else:
        return "odd"
