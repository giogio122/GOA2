# შექმენი ფუნქცია greet და დააბრუნე მისასალმებელი ტექსტი return-ის საშუალებით

def greet():
    return "Hello, welcome!"

message = greet()
print(message)


# შექმენი ფუნქცია add რომელიც დააბრუნებს ორი რიცხვის ჯამს

def add(a, b):
    return a + b

result = add(5, 7)
print(result)


# შექმენი ფუნქცია multiply რომელიც დააბრუნებს ორი რიცხვის ნამრავლს

def multiply(a, b):
    return a * b

result = multiply(4, 6)
print(result)



