#შექმენი ფუნქცია სახელად filter რომელსაც გადაეცემა რიცხვი, თქვენი დავალებაა შეამოწმოთ ეს რიცხვი ლუწია თუ კენტი და დააბრუნოთ შესაბამისი შედეგი even ან odd

def filter(number):
    if number % 2 == 0:
        return "even"
    else:
        return "odd"
