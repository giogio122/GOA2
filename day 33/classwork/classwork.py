# შექმენით ფუნქცია greet და დააბრუნეთ მისასალმებელი ტექსტი return ის საშუალებით


def greet():
    return "Hello, welcome!"

message = greet()
print(message)


#შექმენით ფუნქცია add რომელსაც გადაეცემა ორი რიცხვი და შემდეგ დააბრუნეთ მათი ჯამი


def add(a, b):
    return a + b

result = add(5, 7)
print(result)


#შექმენით ფუნქცია multiply რომელსაც გადაეცემა ორი რიცხვი  შემდეგ კი დააბრუნეთ მათი ნამრავლი


def multiply(a, b):
    return a * b

result = multiply(4, 6)
print(result)



