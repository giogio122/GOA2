def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"
    


def number_to_string(num):
    return str(num)


def bool_to_word(boolean):
    if boolean:
        return "Yes"
    else:
        return "No"
    


def multiply(a, b):
    return a * b
    